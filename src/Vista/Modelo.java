    private void obtenerDatos() throws SQLException{
        List<Modelos> Modelo = new DAOModelo().ObtenerDatos();
        DefaultTableModel modelo = new DefaultTableModel();
        String[] columnas = {"IDModelo","Nombre","Modelo","Medida","Color"};
        
        modelo.setColumnIdentifiers(columnas);
        for (Modelos mod : Modelo){
            String[] renglon = {Integer.toString(mod.getIDModelo()), mod.getNombre(),
                    mod.getModelo(), 
                    mod.getMedida(),mod.getColor()};

            modelo.addRow(renglon);
        }
        jTableModelo.setModel(modelo);
        
        
    }
    public void actualizarModelos() throws SQLException{
        int id = Integer.parseInt(this.jTextIDModelo.getText());
        String nom = this.jTextNombre.getText();
        String mod = this.jTextModelo.getText();
        String med = this.jTextMedida.getText();
        String col = this.jTextColor.getText();
        
        Modelos auto = new Modelos (id, nom, mod, med, 
                col);
        
        DAOModelo dao = new DAOModelo();
        int res = dao.Actualizar(auto);
        if (res == 0){
            JOptionPane.showMessageDialog(rootPane, 
                    "¡El Aotor esta actualizado mi jefe!");
        }else{
            JOptionPane.showConfirmDialog(rootPane, 
                    "¡Mira eso que ocurrio un error!");
        }
     
    }
