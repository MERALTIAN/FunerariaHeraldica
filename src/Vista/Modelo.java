    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        try {
           
           String nom = jTextNombre.getText();
           String mod = jTextModelo.getText();
           String med = jTextMedida.getText();
           String col = jTextColor.getText();
           
           
           if (nom.contentEquals("") || mod.contentEquals("")
                   || med.contentEquals("") || col.contentEquals("")){
               JOptionPane.showMessageDialog(rootPane, 
                       "perro te falta");
               
           }else {
               try {
                  
                   
                   Modelos auto = new Modelos (nom, mod, 
                           med, col);
                   DAOModelo dao = new DAOModelo();
                   if (dao.Insertar(auto) == 0) {
                       JOptionPane.showMessageDialog(rootPane,
                               "Listo agregado papá");
                   }
                   
               } catch (HeadlessException | SQLException e) {
                   JOptionPane.showMessageDialog(rootPane, 
                           "No se agrego el registro nada revisa el codigo");
               }
           }
           obtenerDatos();
           
           limpiarCampos();
       } catch (SQLException ex) {
          Logger.getLogger(Modelo.class.getName()).
                   log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_jButtonAgregarActionPerformed

